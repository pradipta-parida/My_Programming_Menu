"""
String Concatenation - Adding strings together

"""

print("20 days are " + "50" + " minutes")

print("20 days are " + str(50) + " minutes")

print(f"20 days are {50} minutes")  # f(format) our text/string,used to tell {50} is different from the string

print(f"20 days are {20 * 24 * 60} minutes")
